package com.froad.cbank.coremodule.module.normal.user.pojo;

/**
 * 申诉数据实体
 */
public class SafeAppealPojo {

	private Long memberCode;
	
	private String realName;
	
	private String identifyType;
	
	private String identifyNo;
	
	private String identifyPic;
	
	private String mobile;
	
	private String cardNo;
	
	private String demo;

	
	

	public Long getMemberCode() {
		return memberCode;
	}

	public void setMemberCode(Long memberCode) {
		this.memberCode = memberCode;
	}

	public String getRealName() {
		return realName;
	}

	public void setRealName(String realName) {
		this.realName = realName;
	}

	public String getIdentifyType() {
		return identifyType;
	}

	public void setIdentifyType(String identifyType) {
		this.identifyType = identifyType;
	}

	public String getIdentifyNo() {
		return identifyNo;
	}

	public void setIdentifyNo(String identifyNo) {
		this.identifyNo = identifyNo;
	}

	public String getIdentifyPic() {
		return identifyPic;
	}

	public void setIdentifyPic(String identifyPic) {
		this.identifyPic = identifyPic;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getCardNo() {
		return cardNo;
	}

	public void setCardNo(String cardNo) {
		this.cardNo = cardNo;
	}

	public String getDemo() {
		return demo;
	}

	public void setDemo(String demo) {
		this.demo = demo;
	}
	
	
}

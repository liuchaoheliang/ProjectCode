public class ProductBo {
	private String productId;
	private String productName;
	private String categoryId;
	private String money;
	private String vipMoney;
	private String quantity;
	private String vipQuantity;
	private String orgCode;
	private String deliveryMoney;
	public String getProductId() {
		return productId;
	}
	public void setProductId(String productId) {
		this.productId = productId;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getCategoryId() {
		return categoryId;
	}
	public void setCategoryId(String categoryId) {
		this.categoryId = categoryId;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	public String getVipMoney() {
		return vipMoney;
	}
	public void setVipMoney(String vipMoney) {
		this.vipMoney = vipMoney;
	}
	public String getQuantity() {
		return quantity;
	}
	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}
	public String getVipQuantity() {
		return vipQuantity;
	}
	public void setVipQuantity(String vipQuantity) {
		this.vipQuantity = vipQuantity;
	}
	public String getOrgCode() {
		return orgCode;
	}
	public void setOrgCode(String orgCode) {
		this.orgCode = orgCode;
	}
	public String getDeliveryMoney() {
		return deliveryMoney;
	}
	public void setDeliveryMoney(String deliveryMoney) {
		this.deliveryMoney = deliveryMoney;
	}
	
	
}
